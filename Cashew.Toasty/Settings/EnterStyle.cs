namespace Cashew.Toasty.Settings
{
    public enum EnterStyle
    {
        /// <summary>
        /// The toast will appear instantly on the screen.
        /// </summary>
        PopIn,
        /// <summary>
        /// The toast will fade from transparent to opaque.
        /// </summary>
        FadeIn,
        /// <summary>
        /// The toast will slide in from a side of the screen.
        /// </summary>
        SlideIn
    }
}